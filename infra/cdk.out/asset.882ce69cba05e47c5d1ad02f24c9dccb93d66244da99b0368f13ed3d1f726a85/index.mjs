import {
  S3Client,
  GetObjectCommand,
  PutObjectCommand,
  DeleteObjectCommand,
  ListObjectsV2Command
} from "@aws-sdk/client-s3";
var S3Storage = class {
  constructor(bucket2, region2) {
    this.bucket = bucket2;
    this.client = new S3Client({ region: region2 });
  }
  client;
  async readJson(key) {
    try {
      const res = await this.client.send(
        new GetObjectCommand({ Bucket: this.bucket, Key: key })
      );
      const body = await res.Body?.transformToString("utf-8");
      return body ? JSON.parse(body) : null;
    } catch (err) {
      if (err.name === "NoSuchKey") return null;
      throw err;
    }
  }
  async writeJson(key, data) {
    await this.client.send(
      new PutObjectCommand({
        Bucket: this.bucket,
        Key: key,
        Body: JSON.stringify(data, null, 2),
        ContentType: "application/json"
      })
    );
  }
  async readText(key) {
    try {
      const res = await this.client.send(
        new GetObjectCommand({ Bucket: this.bucket, Key: key })
      );
      return await res.Body?.transformToString("utf-8") || null;
    } catch (err) {
      if (err.name === "NoSuchKey") return null;
      throw err;
    }
  }
  async writeText(key, content) {
    await this.client.send(
      new PutObjectCommand({
        Bucket: this.bucket,
        Key: key,
        Body: content,
        ContentType: key.endsWith(".xml") ? "application/rss+xml" : "text/plain"
      })
    );
  }
  async delete(key) {
    await this.client.send(
      new DeleteObjectCommand({ Bucket: this.bucket, Key: key })
    );
  }
  async list(prefix) {
    const res = await this.client.send(
      new ListObjectsV2Command({
        Bucket: this.bucket,
        Prefix: prefix.endsWith("/") ? prefix : `${prefix}/`
      })
    );
    return (res.Contents || []).map((obj) => obj.Key).filter(Boolean);
  }
};

